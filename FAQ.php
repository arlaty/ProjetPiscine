		<h5>Comment devenir vendeur ?</h5>
		<p> 
			Créez-vous un compte en cliquant soit sur l'onglet "Mon Ebay" soit en cliquant sur l'onglet "Vendre". <br>
			Une fois votre profil créé vous pouvez mettre en vente un ou plusieurs articles (un à la fois) en accédant à votre profil, puis en cliquant sur l'onglet "Produits en vente". <br>
			Vous pouvez ajouter un titre, une description, jusqu'à 3 photos et une videos par article, et bien sûre un prix entre 1 centime d'euros et 2 millions d'euros maximum. <br>
			Vous disposez de 3 options de vente : <br>
			- la vente immédiate : vous mettez un prix à l'article que vous souhaitez vendre et l'acheteur payera le prix demandé,<br>
			- les enchères : vous mettez le prix minimum auquel vous souhaitez vendre l'article et vous déterminez une période de vente. Les intéressés enchérissent à partir de celui-ci et à la fin de la période l'administrateur départagera l'acheteur gagnant,<br>
			- la meilleure offre : vous mettez l'article en vente au prix que vous souhaitez en tirer. Les intéressés pourront alors négocier maximum 5 fois avec vous cette offre. Vous aurez accés aux négociations dans l'onglet "Mon Ebay" puis sur "Ventes en cours".<br>
			Attention votre profil peut être supprimer par l'administrateur du site en cas de comportement frauduleux ou malveillant.<br>
		</p>
		<h5>Comment fonctionnent les enchères ?</h5>
		<p>
			Un article mis en vente aux enchères sera disponible sur une période donnée. Chaque acheteur fait une offre à la hauteur de prix le plus élevé auquel il sera prêt à payer et en participant il s'engage à payer l'article s'il gagne l'enchère (d'après la clause d'offre acceptée lors de l'inscrption). C'est pourquoi nous vérifions que l'acheteur dispose des fonds nécessaires sur son compte en banque sans le prélever tout de suite. <br>
			A la fin de la période de vente, l'administrateur s'occupera de déterminer les deux meilleures offres, et l'achteur gagnant paiera 1€ de plus que le montant de la seconde meilleure offre. <br>
		</p>
		<h5>Comment fonctionne l'achat à la meilleure offre ?</h5>
		<p>
			Lors d'une vente à la meilleure offre, le vendeur indique le prix auquel il souhaite vendre son article. L'acheteur s'il souhaite négocier pourra alors faire une offre au vendeur ou bien l'acheter immédiatement au prix donné. Le vendeur pourra alors soit refuser l'offre et annulée la vente soit faire une contre-offre à l'achteur. Durant les négociations, l'acheteur et le vendeur pourront chacun faire 5 contre-offres. Après cela si aucun accord n'a été trouvé la vente sera annulée. <br>
		</p>
